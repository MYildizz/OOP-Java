    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
     
        if (jRadioButton1.isSelected())
        {
            
            
            int age;
            String defict_id,Patient,name,surname,memory_loss,type,cause,location,gender,patient_id;
            
            defict_id=jTextField1.getText();
            memory_loss=jComboBox2.getSelectedItem().toString();
            patient_id=jTextField4.getText();
            
            Patient=jTextField5.getText();
            
            String[] temp=Patient.split(",");
            name=temp[0];
            surname=temp[1];
            
            age=Integer.parseInt(jTextField3.getText());
            gender=jTextField2.getText();
            
            type=jComboBox3.getSelectedItem().toString();
            cause=jComboBox5.getSelectedItem().toString();
            location=jComboBox4.getSelectedItem().toString();
            
            
            
            Amnesia amnesia=new Amnesia(defict_id,memory_loss,type,cause,location,patient_id,name,surname,age,gender);
            
           InfoClass.addMemory(amnesia);
           
           jComboBox1.addItem(defict_id);
           String mesage="\nAmnesia is added ";
           jTextArea1.append(mesage);
           clear();
        }
        
        else if (jRadioButton2.isSelected())
        {
            int age;
            String defict_id,Patient,name,surname,memory_loss,AffectedArea,gender,patient_id;
            
            defict_id=jTextField1.getText();
            memory_loss=jComboBox2.getSelectedItem().toString();
            patient_id=jTextField4.getText();
            
            Patient=jTextField5.getText();
            
            String[] temp=Patient.split(",");
            name=temp[0];
            surname=temp[1];
            
            age=Integer.parseInt(jTextField3.getText());
            gender=jTextField2.getText();
            
            AffectedArea=jComboBox6.getSelectedItem().toString();
            
            Alzheimer alzheimer=new Alzheimer(defict_id,memory_loss,AffectedArea,patient_id,name,surname,age,gender);
            
            InfoClass.addMemory(alzheimer);
            
            
            String mesage="\nAlzheimer is added ";
            jTextArea1.append(mesage);
            
            clear();
        }
       
                
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

